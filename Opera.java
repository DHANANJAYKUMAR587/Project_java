public class Opera {
    public static void main(String[] args) {

        int a = 10, b = 5;

        // Arithmetic Operators
        System.out.println(a + b);  // +
        System.out.println(a - b);  // -
        System.out.println(a * b);  // *
        System.out.println(a / b);  // /
        System.out.println(a % b);  // % {
    
}
}
